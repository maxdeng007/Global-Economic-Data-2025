export function getCountryColors(countryId: string): CountryColors {
  const normalizedId = countryId.toLowerCase().replace(/\s+/g, '-');
  return countryColors[normalizedId] || countryColors.default;
}

export function getCountryGradient(countryId: string): string {
  return getCountryColors(countryId).gradient;
}

export function getDotColors(countryId: string): string[] {
  const colors = getCountryColors(countryId);
  return [colors.primary, colors.secondary, colors.accent];
}
